    ground_2_shower_toilet_facilities TINYINT(1) DEFAULT 0,
    ground_1_hot_tea_facilities TINYINT(1) DEFAULT 0,
    ground_2_hot_tea_facilities TINYINT(1) DEFAULT 0,
