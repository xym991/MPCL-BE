    FOREIGN KEY (treasurer) REFERENCES people(id),
    FOREIGN KEY (welfare_officer) REFERENCES people(id),
    FOREIGN KEY (registrar) REFERENCES people(id),
    FOREIGN KEY (admin) REFERENCES people(id)
